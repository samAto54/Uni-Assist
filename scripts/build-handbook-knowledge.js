const fs = require('fs');
const path = require('path');
const { PDFParse } = require('pdf-parse');

const ROOT = path.resolve(__dirname, '..');
const OUT_JSON = path.join(ROOT, 'assets', 'handbook_knowledge.json');
const OUT_SQL = path.join(ROOT, 'supabase', 'handbook_seed.sql');
const SOURCE_FILES = [
  'SB-JUNE-2-2023-HANDBOOK-Undergrad-programme.pdf',
  '2025-2026-ACADEMIC-CALENDAR-.pdf',
  'gimpa_academic_info.md',
];

function normalizeText(raw) {
  return raw
    .split('\n')
    .map((line) => line.trim())
    .filter(Boolean)
    .filter((line) => !/^--\s*\d+\s+of\s+\d+\s*--$/i.test(line))
    .filter((line) => !/^JUNIOR MEMBERS HANDBOOK$/i.test(line))
    .filter((line) => !/^\(Undergraduate Programmes\)\s*\d*$/i.test(line))
    .join('\n')
    .replace(/\u00ad/g, '')
    .replace(/[ \t]+\n/g, '\n')
    .replace(/\n{3,}/g, '\n\n');
}

function splitBySection(text) {
  const lines = text.split('\n');
  const sections = [];
  let currentSection = 'General';
  let buffer = [];

  const flush = () => {
    const content = buffer.join(' ').replace(/\s+/g, ' ').trim();
    if (content.length > 80) {
      sections.push({ section: currentSection, content });
    }
    buffer = [];
  };

  for (const line of lines) {
    const sectionMatch = line.match(/^(\d{1,2}(?:\.\d+){0,2})\s+(.+)/);
    if (sectionMatch) {
      flush();
      currentSection = `${sectionMatch[1]} ${sectionMatch[2]}`.trim();
      continue;
    }
    buffer.push(line);
  }

  flush();
  return sections;
}

function chunkSection(sourceName, section, content, maxLen = 900) {
  const sentences = content
    .split(/(?<=[.!?])\s+/)
    .map((s) => s.trim())
    .filter(Boolean);

  const chunks = [];
  let current = '';
  let idx = 1;

  for (const sentence of sentences) {
    if ((current + ' ' + sentence).trim().length > maxLen && current) {
      chunks.push({
        source: sourceName,
        section: `${section} (Part ${idx})`,
        content: current.trim(),
      });
      idx += 1;
      current = sentence;
    } else {
      current = current ? `${current} ${sentence}` : sentence;
    }
  }

  if (current.trim()) {
    chunks.push({
      source: sourceName,
      section: `${section} (Part ${idx})`,
      content: current.trim(),
    });
  }

  return chunks;
}

function inferTags(text) {
  const dictionary = [
    'admission', 'registration', 'orientation', 'course load', 'grading', 'exam',
    'attendance', 're-sit', 'retake', 'graduation', 'withdrawal', 'deferment',
    'refund', 'financial aid', 'scholarship', 'loan', 'library', 'clinic',
    'discipline', 'misconduct', 'sanction', 'probation', 'appeal', 'plagiarism'
  ];
  const lowered = text.toLowerCase();
  return dictionary.filter((tag) => lowered.includes(tag));
}

function sqlEscape(value) {
  return value.replace(/'/g, "''");
}

async function main() {
  const rows = [];

  for (const sourceName of SOURCE_FILES) {
    const sourceFile = path.join(ROOT, 'assets', sourceName);
    if (!fs.existsSync(sourceFile)) {
      console.warn(`File not found at ${sourceFile}, skipping.`);
      continue;
    }

    let cleaned = '';
    if (sourceName.endsWith('.pdf')) {
      const dataBuffer = fs.readFileSync(sourceFile);
      const parser = new PDFParse({ data: dataBuffer });
      const parsed = await parser.getText();
      await parser.destroy();
      cleaned = normalizeText(parsed.text || '');
    } else if (sourceName.endsWith('.md')) {
      cleaned = fs.readFileSync(sourceFile, 'utf-8');
    }

    const sections = splitBySection(cleaned);

    for (const item of sections) {
      rows.push(...chunkSection(sourceName, item.section, item.content));
    }
  }

  const deduped = rows.filter((row, index, arr) => {
    const key = `${row.section}|${row.content}`;
    return arr.findIndex((x) => `${x.section}|${x.content}` === key) === index;
  });

  const withTags = deduped.map((row) => ({
    ...row,
    tags: inferTags(`${row.section} ${row.content}`),
  }));

  fs.writeFileSync(OUT_JSON, JSON.stringify(withTags, null, 2));

  const sqlLines = [];
  sqlLines.push('-- Auto-generated by scripts/build-handbook-knowledge.js');
  sqlLines.push('create table if not exists public.ai_knowledge (');
  sqlLines.push('    id          uuid primary key default gen_random_uuid(),');
  sqlLines.push('    source      text not null,');
  sqlLines.push('    section     text not null,');
  sqlLines.push('    content     text not null,');
  sqlLines.push("    tags        text[] not null default '{}',");
  sqlLines.push('    created_at  timestamptz not null default now()');
  sqlLines.push(');');
  sqlLines.push('');
  sqlLines.push('alter table public.ai_knowledge enable row level security;');
  sqlLines.push('');
  sqlLines.push('do $$');
  sqlLines.push('begin');
  sqlLines.push('    if not exists (');
  sqlLines.push("        select 1 from pg_policies where schemaname = 'public' and tablename = 'ai_knowledge' and policyname = 'Anyone can read ai knowledge'");
  sqlLines.push('    ) then');
  sqlLines.push('        create policy "Anyone can read ai knowledge"');
  sqlLines.push('            on public.ai_knowledge for select');
  sqlLines.push('            to authenticated');
  sqlLines.push('            using (true);');
  sqlLines.push('    end if;');
  sqlLines.push('end $$;');
  sqlLines.push('');
  sqlLines.push('do $$');
  sqlLines.push('begin');
  sqlLines.push('    if not exists (');
  sqlLines.push("        select 1 from pg_policies where schemaname = 'public' and tablename = 'ai_knowledge' and policyname = 'Lecturers manage ai knowledge'");
  sqlLines.push('    ) then');
  sqlLines.push('        create policy "Lecturers manage ai knowledge"');
  sqlLines.push('            on public.ai_knowledge for all');
  sqlLines.push("            using ((auth.jwt() -> 'user_metadata' ->> 'role') = 'lecturer')");
  sqlLines.push("            with check ((auth.jwt() -> 'user_metadata' ->> 'role') = 'lecturer');");
  sqlLines.push('    end if;');
  sqlLines.push('end $$;');
  sqlLines.push(
    `delete from public.ai_knowledge where source in (${SOURCE_FILES.map((s) => `'${sqlEscape(s)}'`).join(', ')});`
  );
  sqlLines.push('insert into public.ai_knowledge (source, section, content, tags) values');
  sqlLines.push(
    withTags
      .map((row) => {
        const tagsSql =
          row.tags.length > 0
            ? `array[${row.tags.map((t) => `'${sqlEscape(t)}'`).join(', ')}]`
            : 'array[]::text[]';
        return `('${sqlEscape(row.source)}', '${sqlEscape(row.section)}', '${sqlEscape(row.content)}', ${tagsSql})`;
      })
      .join(',\n') + ';'
  );
  fs.writeFileSync(OUT_SQL, sqlLines.join('\n'));

  console.log(`Generated ${withTags.length} handbook knowledge chunks.`);
  console.log(`JSON: ${OUT_JSON}`);
  console.log(`SQL: ${OUT_SQL}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
